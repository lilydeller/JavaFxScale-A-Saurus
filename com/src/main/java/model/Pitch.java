
package model;
/**
 * {@code Pitch} enum represents the set of all standard musical pitches,
 * including natural notes and enharmonic equivalents (e.g., C♯ and D♭).
*/
public enum Pitch {

    C,
    C_SHARP,
    D_FLAT,
    D,
    D_SHARP,
    E_FLAT,
    E,
    F,
    F_SHARP,
    G_FLAT,
    G,
    G_SHARP,
    A_FLAT,
    A,
    A_SHARP,
    B_FLAT,
    B;
    
}
